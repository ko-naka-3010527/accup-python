from django import forms

class UpdateAccountForm(forms.Form):
    status = forms.IntegerField(max_value=1,min_value=0)
    service = forms.IntegerField(min_value=-1)
    newservice = forms.CharField(max_length=100,required=False)
    accountid = forms.CharField(max_length=100,required=False)
    updatepwd = forms.IntegerField(max_value=1,min_value=0)
    password = forms.CharField(max_length=100,required=False)
    password_conf = forms.CharField(max_length=100,required=False)
    mailaddr1 = forms.IntegerField(min_value=-2)
    newmail1 = forms.EmailField(required=False)
    mailaddr2 = forms.IntegerField(min_value=-2)
    newmail2 = forms.EmailField(required=False)
    mailaddr3 = forms.IntegerField(min_value=-2)
    newmail3 = forms.EmailField(required=False)
    address = forms.IntegerField(min_value=-2)
    newaddress = forms.CharField(max_length=200,required=False)
    phonenum = forms.IntegerField(min_value=-2)
    newphonenum = forms.CharField(max_length=20,required=False)
    link1 = forms.IntegerField(min_value=-2)
    link2 = forms.IntegerField(min_value=-2)
    link3 = forms.IntegerField(min_value=-2)
    multifactor_type = forms.CharField(max_length=100,required=False)
    multifactor_info = forms.CharField(max_length=100,required=False)
    secq1 = forms.CharField(max_length=200,required=False)
    seca1 = forms.CharField(max_length=200,required=False)
    secq2 = forms.CharField(max_length=200,required=False)
    seca2 = forms.CharField(max_length=200,required=False)
    secq3 = forms.CharField(max_length=200,required=False)
    seca3 = forms.CharField(max_length=200,required=False)
    register_date = forms.DateField(required=False)
    unregister_date = forms.DateField(required=False)
    memo = forms.CharField(required=False)

